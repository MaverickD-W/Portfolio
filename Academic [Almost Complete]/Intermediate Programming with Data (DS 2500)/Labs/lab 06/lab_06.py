# PROBLEM 1

def find_average(lst1):
  if lst1 == []:
    return None
  else:
    mean = sum(lst1)/(len(lst1))
    return mean

# PROBLEM 2

def find_median(lst2):
  if lst2 == []:
    return None
  else:
    while len(lst2) > 2:
      lst2.remove(lst2[0])
      lst2.remove(lst2[(len(lst2)-1)])
    med = sum(lst2)/len(lst2)
    return med

