def find_mode(lst3):
  if lst3 == []:
    return None
  else:
    counts = []
    mode = []
    for a in lst3:
      counts.append(lst3.count(a))
    for b in range(len(lst3)):
      if counts[b] == max(counts):
        mode.append(lst3[b])
    if len(mode) == 1:
      return mode[0]
    else:
      for c in mode:
        while mode.count(c) > 1:
          mode.remove(c)
      if len(mode) == 1:
        return mode[0]
      else:
        return mode

# PROBLEM 4

def find_variance(lst4):
  if lst4 == []:
    return None
  else:
    avg = find_average(lst4)
    var = 0
    for i in lst4:
      var += ((i-avg)**2)/len(lst4)
    return var

