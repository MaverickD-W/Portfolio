# Function: Reading csvs and merging the resulting DataFrames

def merge_dfs(df1, df2, shared_columns, suffix1, suffix2):     # TAKES two csv files, name(s) of their shared column(s), and column suffix names, RETURNS a merged DataFrame of the two csv files
  merged_df = df1.merge(df2,on=shared_columns, suffixes=(suffix1,suffix2))      # Merges "df1" and "df2" on "shared_columns" with inputted suffixes and stores it as "merged_df"
  return merged_df

# Function: Selects columns in the inputted DataFrame with values from 2024

def select_columns(df):               # TAKES a DataFrame, RETURNS a list of the defined shared columns between merged DataFrames plus the columns with data from 2024
  year_range = ["2019", "2020", "2021", "2022", "2023", "2024", "2025"]                   # Defines years covered by new DataFrame
  cols = ["RegionID", "SizeRank", "RegionName", "RegionType", "StateName"]
  for a in df:                        # Iterates through each column in the inputted DataFrame
    for b in year_range:
      if b in a:                      # If 2024 is in the column name, which is a string of the date during which the data was collected
        cols.append(a)           # Add the columns with data from 2024 to the list of shared columns
  return cols

# Function: Cleans data within a DataFrame by eliminating rows with 0 and null values

def clean_df(df):                                       # TAKES a DataFrame, RETURNS a cleaned version of the DataFrame without rows containing null/empty or 0 values
  deletes = []
  for a in df:                                          # Iterates through each title in the inputted DataFrame
    for b in range(len(df)):                            # Iterates through the number of rows in "df"
      try:
        if float(df[a][b]) == 0.0:                # If float of the value in column a, row b is 0.0 and if the row is not already in list "deletes", add row b to "deletes"
          if deletes.count(b) < 1:
            deletes.append(b)
      except:
        None

  df_cleaned = pd.DataFrame(data=df).drop(deletes)      # Drop the rows that contain 0s, which are stored in list "deletes", and store new DataFrame as "df_cleaned"
  df_cleaned = df_cleaned.dropna()                      # Drop null values in "df_cleaned"
  return df_cleaned

