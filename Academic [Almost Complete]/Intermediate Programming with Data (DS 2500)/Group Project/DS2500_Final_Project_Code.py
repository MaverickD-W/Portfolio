# Function to create scatterplots with two legend boxes
def plot_scatter_two_legends(x_col, title, x_label):
    plt.figure(figsize=(16,8))
    ax = plt.gca()  # get current axes

    # Plot Other MSAs
    sns.scatterplot(data=others, x=x_col, y="Sales_Count",
                    color="lightgrey", alpha=0.3, ax=ax)

    # Plot Top 100 group
    sns.scatterplot(data=top_100, x=x_col, y="Sales_Count",
                    color="green", s=70, ax=ax)

    # Plot Top 10 individually
    top10_colors = sns.color_palette("tab10", 10)
    top10_handles = []
    for i, (idx, row) in enumerate(top_10.iterrows()):
        sc = ax.scatter(row[x_col], row["Sales_Count"],
                        color=top10_colors[i], s=120)
        top10_handles.append(sc)

    # First legend: Top 10 MSAs
    top10_labels = list(top_10["RegionName"])
    legend1 = ax.legend(handles=top10_handles, labels=top10_labels, title="Top 10 MSAs",
                        bbox_to_anchor=(1.05, 1), loc='upper left', fontsize=9)
    ax.add_artist(legend1)  # keep first legend visible

    # Second legend: Groups
    group_handles = [
        mpatches.Patch(color="green", label="Top 100 (excluding Top 10)"),
        mpatches.Patch(color="lightgrey", label="Other MSAs")
    ]
    ax.legend(handles=group_handles, title="Groups",
              bbox_to_anchor=(1.05, 0.5), loc='upper left', fontsize=9)

    plt.xlabel(x_label)
    plt.ylabel("Sales Count")
    plt.title(title)
    plt.tight_layout()
    plt.show()

