def longest_streak(team_lst):
  max_streak = []
  for b in range(len(team_lst)):
    sum1 = 0
    streaks = []
    team = Team(team_lst[b])                      # Defining Team() class "name" input
    for c in range(1,23):
      goal = WSL_goals[str(c)][b]                 # Assigning each goal by the given team before appending it to team.goals
      if str(goal) != "nan":
        team.add_goals(int(goal))                 # Inputing a given teams goals, if listed
      else:
        team.add_goals(0)                         # Unlisted goals are 0
    for d in team.goals:
      if d >= 2:                                  # Adding each goal > 2 to the count/sum of streaks
        sum1 += 1
      else:
        streaks.append(sum1)                      # If the number of goals is < 2, append the streak length before this goal to a list of streaks
        sum1 = 0                                  # If the number of goals is < 2, reset the streak count
    streaks.append(sum1)                          # Append the last streak to the list of streaks for the given team
    max_streak.append(max(streaks))               # Append the maximum streak for each team to a list

  for e in range(len(max_streak)):                # Finding the team which has the maximum streak
    if max_streak[e] == max(max_streak):
      max_s = team_lst[e]
  return max_s

