def most_consistent_scoring(team_lst):
  con_list = []
  for b in range(len(team_lst)):
    team = Team(team_lst[b])                      # Defining Team() class "name" input
    for c in range(1,23):
      goal = WSL_goals[str(c)][b]                 # Assigning each goal by the given team before appending it to team.goals
      if str(goal) != "nan":
        team.add_goals(int(goal))                 # Inputing a given teams goals, if listed
      else:
        team.add_goals(0)                         # Unlisted goals are 0
    avg = team.get_avg(team.goals)                # Finding the average number of goals by the given team
    var = team.get_var(team.goals,avg)            # Finding the variance in the number of goals by the given team
    sd = team.get_sd(var)                         # Finding the standard deviation of the number of goals by the given team
    con = sd/avg                                  # Finding the coeffecient of variance = standard_deviation/mean
    con_list.append(con)                          # Appending the coeffecient of variance for each team to a list

  min_con = min(con_list)                         # Finding the lowest coefficient of variance
  for f in range(len(con_list)):                  # Finding the team which has the lowest coeffecient of variance, which determines the most consistent goal-scoring pattern
    if con_list[f] == min_con:
      min_con = team_lst[f]
  return min_con

