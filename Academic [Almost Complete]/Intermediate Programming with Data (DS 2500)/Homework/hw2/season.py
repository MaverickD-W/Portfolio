def cumulative_goals(team_lst):                                 # Creates a list of lists with the cumulative goals of every team. Each list entry is a sum of goals from the current match and all prior matches.
  goals_list1 = []
  goals_list2 = []
  for b in range(len(team_lst)):
    teams = Team(team_lst[b])                                   # Defining Team() class "name" input
    for c in range(1,23):
      if str(WSL_goals[str(c)][b]) != "nan":
        teams.add_goals(int(WSL_goals[str(c)][b]))              # Inputing a given teams goals, if listed
      else:
        teams.add_goals(0)                                      # Unlisted goals are 0
      teams.move_next()                                         # Using defined function to increase the value of teams.x by each additional goal in team.goals for each team
      goals_list1.append(teams.x)                               # Appending every new teams.x for each team, resulting in a list of increasing goal totals for all teams

  for d in range(len(team_lst)):
    goals_list2.append((goals_list1[22*d:22*(d+1)]))            # Creating a list of lists with every team.x for each team

  return goals_list2

def plot_teams(team_lst,team_abbrevs,colors_lst):               # Creates a scatter plot of the teams' total_goals by each match
  goals_list2 = cumulative_goals(team_lst)
  for d in range(len(goals_list2[0])):                          # for d in range(22)    (number of matches)
    for e in range(len(team_lst)):                              # for e in range(12)    (number of teams)
      teams = Team(team_lst[e])                                 # Defining Team() class "name" input, where each new iteration of loop is a new team and an emptied teams.goals
      teams.add_goals(goals_list2[e][d])                        # Appending the given team's total goals by match d to the teams.goals
      teams.y = team_abbrevs[e]                                 # Assigning the y-value, teams.y, of the graph as the given team's abbreviation
      teams.color = colors_lst[e]                               # Assigning the corresponding team color for the given team
      teams.move_next()                                         # Defining teams.x as the value in teams.goals (the given team's total goals by match d)
