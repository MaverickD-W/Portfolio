import pandas as pd
df = pd.DataFrame({
    "A": [i for i in range(1,6)],
    "B": [4, None, None, None, None],
    "C": ["High", "Medium", "Medium", "High", "Low"]
})

deletes = []
for i in df:
  if df[i].isnull().sum() > 0:
    if deletes.count(i) < 1:
      deletes.append(i)

df_col_cleaned = pd.DataFrame(data=df).drop(columns=deletes)
df_col_cleaned

deletes2 = []
for i in df:
  for j in range(len(df[i])):
    if df[j:(j+1)][i].isnull().sum() > 0:
      if deletes2.count(j) < 1:
        deletes2.append(j)

df_row_cleaned = pd.DataFrame(data=df).drop(deletes2)
df_row_cleaned

