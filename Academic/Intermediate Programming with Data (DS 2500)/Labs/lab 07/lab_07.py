def zscore(lst1):
  if lst1 == []:
    return None
  else:
    avg = sum(lst1)/(len(lst1))
    sd = 0
    for a in lst1:
      sd += ((a-avg)**2)/len(lst1)
    sd = sd**(1/2)
    z = []
    for b in lst1:
      z.append((b-avg)/sd)
    return z

