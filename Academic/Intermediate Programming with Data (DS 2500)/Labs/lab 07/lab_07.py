def min_max_scaling(lst2):
  if lst2 == []:
    return None
  else:
    min_num = min(lst2)
    max_num = max(lst2)
    min_max = []
    for i in lst2:
      min_max.append((i - min_num)/(max_num - min_num))
    return min_max

