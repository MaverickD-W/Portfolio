def list_abalone_values(df, ab_id):             # TAKES a DataFrame and abalone_id value, RETURNS list of values (not "sex" nor "abalone_id") in row with abalone_id "ab_id"
  lst1 = []
  for g in df:                                  # Iterates through titles in DataFrame "df"
    if g != "abalone_id" and g != "sex":        # Excludes columns "abalone_id" and "sex"
      lst1.append(float(df[g][ab_id-1]))        # Adds to an empty list the value in column g for the row with abalone_id "ab_id"
  return lst1

def euclidean_distance(list1, list2):           # TAKES in two lists, RETURNS the Euclidean Distance between the two lists (rounded to two decimal places)
  eu_dist = (sum((list1[d] - list2[d])**2 for d in range(len(list1))))**(1/2)
  return round(float(eu_dist),2)
pass

def manhattan_distance(list1, list2):           # TAKES in two lists, RETURNS the Manhattan Distance between the two lists (rounded to two decimal places)
  man_dist = sum(abs(list1[e] - list2[e]) for e in range(len(list1)))
  return round(float(man_dist),2)
