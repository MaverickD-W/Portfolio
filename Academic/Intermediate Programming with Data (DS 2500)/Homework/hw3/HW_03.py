def min_max_scale(df, lst):                  # TAKES a DataFrame and a list, RETURNS a Min-Max Scaled version of the list based on the scaled DataFrame
  df_values = []
  for a in df['abalone_id']:                 # Iterates through the values in the "abalone_id" column of the DataFrame
    row = []                                      # The empty list row is defined and reset for each new value of a
    for b in df:                             # Iterates through each column title of the DataFrame
      if b != "sex" and b != "abalone_id":        # Selects all columns other than "sex" and "abalone_id"
        row.append(float(df[b][a-1]))        # Adds to the list "row" the value in each column (not "sex" or "abalone_id") of a given row (row with "abalone_id" a)
    df_values.append(row)                           # Adds list "row" to list "ab_data" for each new value of a (each row), so each list within ab_data is a row (not "sex" or "abalone_id") of values from the DataFrame

  mms = MinMaxScaler()                            # Define MinMaxScaler() function
  mms.fit(df_values)                              # Fits data for scaler function to the list of values from the inputed DataFrame
  new_lst = mms.transform([lst])                  # Scales data in "lst" based on fitted scaler function
  new_lst = new_lst[0]                            # Unembeds "new_lst", the list of the list of scaled values in "lst"
  return new_lst

def euclidean_min_max(df, list1, list2):     # TAKES a Dataframe and two lists, RETURNS the Euclidean Distance between the Min-Max Scaled versions of the inputed lists
  list3 = min_max_scale(df, list1)           # Applies Min-Max Scaling to the first inputted list
  list4 = min_max_scale(df, list2)           # Applies Min-Max Scaling to the second inputted list
  return euclidean_distance(list3,list4)          # Takes and returns the Euclidean Distance of the scaled lists

def manhattan_min_max(df, list1, list2):     # TAKES a Dataframe and two lists, RETURNS the Manhattan Distance between the Min-Max Scaled versions of the inputed lists
  list3 = min_max_scale(df, list1)           # Applies Min-Max Scaling to the first inputted list
  list4 = min_max_scale(df, list2)           # Applies Min-Max Scaling to the second inputted list
  return manhattan_distance(list3,list4)          # Takes and returns the Manhattan Distance of the scaled lists

