def read_and_merge_csv(csv1, csv2, shared_column):    # TAKES two csv files and the name of their shared column, RETURNS a merged DataFrame of the two csv files
  df1 = pd.read_csv(csv1)                             # Reads the first csv file and stores it as DataFrame "df1"
  df2 = pd.read_csv(csv2)                             # Reads the second csv file and stores it as DataFrame "df2"
  merged_df = df1.merge(df2,on=shared_column)         # Merges "df1" and "df2" on "shared_column" and stores it as "merged_df"
  return merged_df

def clean_df(df):                                       # TAKES a DataFrame, RETURNS a cleaned version of the DataFrame without rows containing null/empty or 0 values
  deletes = []
  for a in df:                                          # Iterates through each title in the inputted DataFrame
    for b in range(len(df)):                            # Iterates through the number of rows in "df"
      try:
        if float(df[a][b]) == 0.0:                # If float of the value in column a, row b is 0.0 and if the row is not already in list "deletes", add row b to "deletes"
          if deletes.count(b) < 1:
            deletes.append(b)
      except:
        None

  df_cleaned = pd.DataFrame(data=df).drop(deletes)      # Drop the rows that contain 0s, which are stored in list "deletes", and store new DataFrame as "df_cleaned"
  df_cleaned = df_cleaned.dropna()                      # Drop null values in "df_cleaned"
  return df_cleaned

