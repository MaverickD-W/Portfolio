def mean_sq_err_and_r2_score(dftrain, dftest):                  # TAKES a training and test set, RETURNS the mean squared error and the r2-score based on the test set
  x_tr1 = dftrain[["shell_weight"]].values                      # Defines x_train
  y_tr1 = dftrain["age"].values                                 # Defines y_train
  x_te1 = dftest[["shell_weight"]].values                       # Defines x_test
  y_te1 = dftest["age"].values                                  # Defines y_test

  model1 = LinearRegression()                                   # Define LinearRegression() function
  model1.fit(x_tr1,y_tr1)                                       # Fits data for linear regression
  y_pred1 = model1.predict(x_te1)                               # Predicts age based on test values of shell weight
  mean_sq_err = round(mean_squared_error(y_te1, y_pred1),2)     # Calculates and rounds mean squared error of age test value and predicted age
  r2_sc = round(r2_score(y_te1, y_pred1),2)                     # Calculates and rounds r2-score of age test value and predicted age

  return (mean_sq_err, r2_sc)

